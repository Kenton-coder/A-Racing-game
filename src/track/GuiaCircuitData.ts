    /**
     * 取得指定兩路點之間的直線距離（公尺）。
     */
    public static GetSegmentDistance(indexA: number, indexB: number): number {
        if (indexA < 0 || indexA >= this.WAYPOINT_COUNT ||
            indexB < 0 || indexB >= this.WAYPOINT_COUNT) {
            console.warn(`[GuiaCircuitData] 路點索引超出範圍：A=${indexA}, B=${indexB}，有效範圍 0~${this.WAYPOINT_COUNT - 1}`);
            return 0;
        }
        const p1 = this.Waypoints[indexA];
        const p2 = this.Waypoints[indexB];
        return Math.hypot(p2.x - p1.x, p2.y - p1.y);
    }

    /**
     * 取得指定賽車模式的賽道半寬度（中心線到護牆距離，公尺）。
     */
    public static GetHalfTrackWidth(mode: RacingMode): number {
        return this.TrackWidth[mode] * 0.5;
    }

    /**
     * 取得下一個路點索引，自動環繞回起點（用於 AI 和計圈系統）。
     */
    public static GetNextWaypointIndex(currentIndex: number): number {
        if ((this.WAYPOINT_COUNT as number) === 0) return 0;
        return (currentIndex + 1) % this.WAYPOINT_COUNT;
    }

    /**
     * 取得指定路點所在路段的名稱（繁中 / 英）。
     */
    public static GetSectionNameAtWaypoint(waypointIndex: number): string {
        let lastKey = 0;
        const keys = Object.keys(this.SectionNames).map(Number).sort((a, b) => a - b);
        for (const key of keys) {
            if (key <= waypointIndex) {
                lastKey = key;
            } else {
                break;
            }
        }
        return this.SectionNames[lastKey] || "未知路段";
    }

    /**
     * 將速度從 km/h 換算為 m/s（用於物理運算）。
     */
    public static KmhToMs(kmh: number): number {
        return kmh / 3.6;
    }

    /**
     * 將速度從 m/s 換算為 km/h（用於 HUD 顯示）。
     */
    public static MsToKmh(ms: number): number {
        return ms * 3.6;
    }
